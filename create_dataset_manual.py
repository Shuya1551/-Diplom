def random_date(start_year=2025, end_year=2030):
    start = datetime(start_year, 1, 1)
    end = datetime(end_year, 12, 31)
    return start + timedelta(days=random.randint(0, (end-start).days))

def random_time():
    return f"{random.randint(9, 20):02d}:{random.choice(['00','15','30','45'])}"

def generate_news(plan):
    title = plan['title']
    date = plan['date']
    time_start = plan['time_start']
    time_end = plan.get('time_end')
    location = plan['location']
    participants = plan['participants']
    description = plan['description']
    audience = plan['audience']
    topic = plan['topic']
    
    templates = [
        f"{date} в {time_start} в {location} прошёл(ла) {title}. {description} Участие приняли {participants} {audience}.",
        f"Состоялся {title}, который собрал {participants} {audience}. Мероприятие прошло {date} в {time_start} в {location}. {description}",
        f"{date} года в {location} стартовал(а) {title}. {description} В мероприятии участвовало {participants} человек.",
        f"{title} состоялся {date} в {time_start}. Место проведения: {location}. {description} Участники – {audience} – высоко оценили программу.",
        f"{title} {description} Мероприятие объединило {participants} {audience}. Дата: {date}, начало в {time_start}, место – {location}."
    ]
    template = random.choice(templates)
    if time_end:
        template += f" Завершится в {time_end}."
    # Контроль длины 150-300 символов
    if len(template) < 150:
        template += " Организаторы благодарят всех участников."
    if len(template) > 300:
        template = template[:297] + "..."
    return template

def generate_plan(idx):
    topic_type, topic_name, desc, aud = random.choice(TOPICS)
    title = f"{topic_type.capitalize()} «{topic_name.capitalize()}»"
    date_obj = random_date()
    date_str = date_obj.strftime("%d.%m.%Y")
    time_start = random_time()
    time_end = random_time() if random.random() > 0.3 else ""  # 70% с окончанием
    location = random.choice(CITIES)
    participants = random.randint(10, 800)  # уникальные числа
    description = random.choice(DESC_TOPICS).capitalize() + "."
    audience = random.choice(AUDIENCES)
    category = random.choice(CATEGORIES)
    goal = random.choice(GOALS)
    organizer = random.choice(ORGANIZERS)
    speaker = random.choice(SPEAKERS) if random.random() > 0.5 else ""  # 50% со спикером
    format_type = random.choice(FORMATS)
    
    # Пропускаем некоторые поля с вероятностью 10-20%
    if random.random() < 0.1:
        location = ""
    if random.random() < 0.15:
        time_start = ""
        time_end = ""
    if random.random() < 0.2:
        participants = ""
    if random.random() < 0.1:
        description = ""
    if random.random() < 0.15:
        goal = ""
    if random.random() < 0.2:
        audience = ""
    if random.random() < 0.2:
        organizer = ""
    if random.random() < 0.2:
        speaker = ""

    plan = {
        "title": title,
        "date": date_str,
        "time_start": time_start,
        "time_end": time_end,
        "location": location,
        "participants": str(participants) if participants else "",
        "description": description,
        "audience": audience,
        "topic": topic_name,
        "format": format_type,
        "category": category,
        "goal": goal,
        "organizer": organizer,
        "speaker": speaker,
    }
    return plan

def plan_to_input_text(plan):
    parts = []
    if plan['title']:
        parts.append(f"Название мероприятия: {plan['title']}")
    if plan['date']:
        parts.append(f"Дата: {plan['date']}")
    if plan['time_start']:
        parts.append(f"Время: {plan['time_start']}")
    if plan['time_end']:
        parts.append(f"Окончание: {plan['time_end']}")
    if plan['location']:
        parts.append(f"Место проведения: {plan['location']}")
    if plan['format']:
        parts.append(f"Формат: {plan['format']}")
    if plan['category']:
        parts.append(f"Категория: {plan['category']}")
    if plan['participants']:
        parts.append(f"Количество участников: {plan['participants']}")
    if plan['description']:
        parts.append(f"Описание: {plan['description']}")
    if plan['goal']:
        parts.append(f"Цель: {plan['goal']}")
    if plan['speaker']:
        parts.append(f"Спикер(ы): {plan['speaker']}")
    if plan['audience']:
        parts.append(f"Аудитория: {plan['audience']}")
    if plan['organizer']:
        parts.append(f"Организатор: {plan['organizer']}")
    return ". ".join(parts) + ". Новость:"

def main():
    num_examples = 200
    with open("manual_pairs.csv", "w", newline="", encoding="utf-8") as f:
        writer = csv.writer(f)
        writer.writerow(["input_text", "output_text"])
        for i in range(num_examples):
            plan = generate_plan(i)
            input_text = plan_to_input_text(plan)
            output_text = generate_news(plan)
            writer.writerow([input_text, output_text])
            if (i+1) % 50 == 0:
                print(f"Сгенерировано {i+1} / {num_examples}")
    print(f"✅ Создано {num_examples} примеров в manual_pairs.csv")

if __name__ == "__main__":
    main()